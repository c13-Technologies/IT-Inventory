// ----------------------------------------------------------------------
// JSON API helper. Centralises the (requireAuth + can(...)) gate so
// every /api/* endpoint below is RBAC-enforced by construction — a new
// JSON route is a one-liner via apiRoute(method, slug, subPath, perm,
// handler). Direct app.get/app.post(...) calls with paths starting
// /api/ are forbidden by convention; reviewers should reject additions
// that bypass this gate.
//   method      — 'get'|'post'|'put'|'patch'|'delete'
//   slug        — namespace (sidebar CRUD slug OR dedicated JSON slug)
//   subPath     — optional URL tail (default '')
//   permSuffix  — 'read'|'write' (gate is `<slug>:<permSuffix>`)
//   handler     — async (req, res, next) => ...
// ----------------------------------------------------------------------
function apiRoute(method, slug, subPath, permSuffix, handler) {
  const path = '/api/' + slug + (subPath || '');
  app[method](path, requireAuth, can(slug + ':' + permSuffix), handler);
}

// API — dashboard statistics (used by the ApexCharts dashboard.init.js)
apiRoute('get', 'dashboard', '/stats', 'read', async (_req, res) => {
  try {
    const tid = _req.session.tenantId;
    const [
      allAssets,
      totalUsers,
      totalVendors,
      totalLocations,
      recentAudit,
    ] = await Promise.all([
      prisma.asset.findMany({ where: { tenantId: tid }, select: { status: true, categoryId: true } }),
      prisma.user.count({ where: { tenantId: tid } }),
      prisma.vendor.count({ where: { tenantId: tid } }),
      prisma.location.count({ where: { tenantId: tid } }),
      prisma.auditLog.findMany({ where: { tenantId: tid }, include: { user: true }, orderBy: { createdAt: 'desc' }, take: 10 }),
    ]);

    // Group by status in JS
    const statusMap = {};
    allAssets.forEach(a => {
      statusMap[a.status] = (statusMap[a.status] || 0) + 1;
    });
    const assetsByStatus = Object.entries(statusMap).map(([status, count]) => ({ status, count }));

    // Group by category in JS
    const catMap = {};
    allAssets.forEach(a => {
      if (a.categoryId) catMap[a.categoryId] = (catMap[a.categoryId] || 0) + 1;
    });
    // Resolve category names
    const categoryIds = Object.keys(catMap);
    const categories = categoryIds.length
      ? await prisma.category.findMany({ where: { id: { in: categoryIds } } })
      : [];
    const nameMap = {};
    categories.forEach(c => { nameMap[c.id] = c.name; });
    const assetsByCategory = Object.entries(catMap)
      .map(([catId, count]) => ({ category: nameMap[catId] || 'Uncategorized', count }))
      .sort((a, b) => b.count - a.count)
      .slice(0, 8);

    res.json({
      totalAssets: allAssets.length,
      assetsByStatus,
      assetsByCategory,
      totalUsers,
      totalVendors,
      totalLocations,
      recentActivity: recentAudit.map(e => ({
        action: e.action,
        entityType: e.entityType,
        entityId: e.entityId,
        userName: e.user ? e.user.fullName : 'System',
        createdAt: e.createdAt,
      })),
    });
  } catch (err) {
    console.error('Dashboard stats error:', err);
    res.status(500).json({ error: 'Failed to load dashboard statistics.' });
  }
});

// API — dashboard 7-day trends (sparkline data for the stat cards).
//   Returns counts per day for the last 7 days (oldest -> newest).
//   Each metric is numerically aligned with the `labels` array so the
//   client can render Apache-style sparklines without re-pivoting.
apiRoute('get', 'dashboard', '/trends', 'read', async (_req, res) => {
  try {
    const tid = _req.session.tenantId;
    const DAYS = 7;
    // Build the 7 day-buckets (midnight..midnight) ending today, oldest first.
    const now = new Date();
    const startOfToday = new Date(now.getFullYear(), now.getMonth(), now.getDate());
    const buckets = [];
    for (let i = DAYS - 1; i >= 0; i--) {
      const d = new Date(startOfToday);
      d.setDate(d.getDate() - i);
      buckets.push(d);
    }
    const from = buckets[0];
    const labels = buckets.map(d => d.toISOString().slice(0, 10));
    const inWindow = { createdAt: { gte: from } };

    const [assetsRaw, usersRaw, assignmentsRaw, vendorsRaw] = await Promise.all([
      prisma.asset.findMany({ where: { tenantId: tid, ...inWindow }, select: { createdAt: true } }),
      prisma.user.findMany({ where: { tenantId: tid, ...inWindow }, select: { createdAt: true } }),
      // SECURITY/CORRECTNESS: filter assignments by `assignedAt` (the
      // business-meaningful date) not `createdAt` (the row-creation
      // date). The shared `inWindow` filter is `createdAt >= from`,
      // which would silently drop any assignment whose row was created
      // in the last 7 days but was assigned earlier (and would also
      // pick up old assignments that just happened to be re-saved).
      // The sparkline then bucketises by `assignedAt`, so the WHERE
      // and the GROUP BY must agree on which date they're using.
      prisma.assignment.findMany({ where: { tenantId: tid, assignedAt: { gte: from } }, select: { assignedAt: true } }),
      prisma.vendor.findMany({ where: { tenantId: tid, ...inWindow }, select: { createdAt: true } }),
    ]);

    // Bucket-by-day helper. Counts rows whose date falls in [buckets[i], buckets[i+1]).
    function bucketize(records, getDate) {
      const counts = new Array(DAYS).fill(0);
      for (const r of records) {
        const d = getDate(r);
        if (!d) continue;
        const t = d.getTime();
        for (let i = 0; i < DAYS; i++) {
          const lo = buckets[i].getTime();
          const hi = i + 1 < DAYS ? buckets[i + 1].getTime() : startOfToday.getTime() + 24 * 60 * 60 * 1000;
          if (t >= lo && t < hi) { counts[i]++; break; }
        }
      }
      return counts;
    }

    res.json({
      labels,
      assets:       bucketize(assetsRaw, r => r.createdAt),
      users:        bucketize(usersRaw, r => r.createdAt),
      assignments:  bucketize(assignmentsRaw, r => r.assignedAt),
      vendors:      bucketize(vendorsRaw, r => r.createdAt),
    });
  } catch (err) {
    console.error('Dashboard trends error:', err);
    res.status(500).json({ error: 'Failed to load dashboard trends.' });
  }
});

