// Express + EJS server for the IT Inventory dashboard.
// - EJS views live in views/pages/ and share partials from views/partials/
//   (head, topbar, sidebar, footer, scripts, scripts-auth).
// - Static assets (assets/, fonts.*/, _DataURI/, docs/, prisma/, etc.) are
//   served from the project root.
//
// Usage:
//   npm start        # production-style, no auto-reload
//   npm run dev      # nodemon watches this file and the static assets
//
// Then open http://127.0.0.1:3000/

const fs = require('fs');
const path = require('path');
const express = require('express');

const app = express();

// Don't advertise the runtime in responses.
app.disable('x-powered-by');

// EJS view engine. The pages/ folder is the implicit root, so a page is
// rendered as res.render('pages/<name>').
app.set('view engine', 'ejs');
app.set('views', path.join(__dirname, 'views'));

// page-name -> human title map. Generated by scripts/convert-to-ejs.js so that
// the <title> tag in the head partial can show the right page name.
const TITLES = JSON.parse(
  fs.readFileSync(path.join(__dirname, 'views', 'titles.json'), 'utf8')
);

const PORT = Number(process.env.PORT) || 3000;
const HOST = process.env.HOST || '127.0.0.1';
const MAX_PORT_ATTEMPTS = 10;
const ROOT_DIR = __dirname;

// Disable HTTP caching in dev so every refresh picks up the latest HTML/CSS/JS.
app.use((req, res, next) => {
  res.set('Cache-Control', 'no-store, no-cache, must-revalidate, proxy-revalidate');
  res.set('Pragma', 'no-cache');
  res.set('Expires', '0');
  next();
});

// Request log – one line per request, no extra dep.
app.use((req, _res, next) => {
  console.log(`[${new Date().toISOString()}] ${req.method} ${req.url}`);
  next();
});

// ----------------------------------------------------------------------
// EJS routes — must come BEFORE the static middleware so that .html URLs
// render the matching views/pages/<name>.ejs instead of the (now removed)
// root HTML files.
// ----------------------------------------------------------------------

// Root
app.get('/', (_req, res) => {
  res.render('pages/index', { title: TITLES['index'] || 'Dashboard' });
});

// Error pages — set the right HTTP status before rendering the template.
app.get('/pages-404.html', (_req, res) => {
  res.status(404).render('pages/pages-404', { title: TITLES['pages-404'] || '404' });
});
app.get('/pages-500.html', (_req, res) => {
  res.status(500).render('pages/pages-500', { title: TITLES['pages-500'] || '500' });
});

// All other *.html URLs → render the matching EJS view.
app.get('/:page.html', (req, res, next) => {
  const page = req.params.page;
  // Defensive: only allow simple, single-segment page names. This blocks any
  // attempt to escape views/pages/ (e.g. via ".." or path tricks) and ensures
  // we only ever render files we actually generated from the Minia template.
  if (!/^[a-zA-Z0-9_-]+$/.test(page)) return next();
  const viewPath = path.join(__dirname, 'views', 'pages', page + '.ejs');
  if (fs.existsSync(viewPath)) {
    res.render('pages/' + page, { title: TITLES[page] || page });
  } else {
    next(); // fall through to 404
  }
});

// ----------------------------------------------------------------------
// Static assets — every other file in the project root (assets/, fonts.*/,
// _DataURI/, docs/, prisma/, server.js, package.json, etc.). We do NOT
// serve *.html here; the EJS routes above own those URLs.
// ----------------------------------------------------------------------
app.use(express.static(ROOT_DIR, {
  index: false,      // '/' is handled by the explicit route above
  extensions: false,  // don't auto-resolve /foo → /foo.html
}));

// Final fallback: render the 404 EJS view with a 404 status.
app.use((_req, res) => {
  res.status(404).render('pages/pages-404', { title: TITLES['pages-404'] || '404' });
});

// Try to bind to PORT; if it's busy (EADDRINUSE), increment the port and retry.
// Prevents nodemon from crash-looping when 3000 is held by another app.
function listenWithFallback(app, startPort, host, maxAttempts) {
  return new Promise((resolve, reject) => {
    let port = startPort;
    let attempts = 0;

    const tryOnce = () => {
      const server = app.listen(port, host);

      server.once('listening', () => {
        resolve({ server, port, attempts });
      });

      server.once('error', (err) => {
        if (err.code === 'EADDRINUSE' && attempts < maxAttempts) {
          attempts += 1;
          console.warn(`[server] Port ${port} is in use, trying ${port + 1}...`);
          port += 1;
          setImmediate(tryOnce);
        } else {
          reject(err);
        }
      });
    };

    tryOnce();
  });
}

listenWithFallback(app, PORT, HOST, MAX_PORT_ATTEMPTS)
  .then(({ server, port, attempts }) => {
    if (attempts > 0) {
      console.log(`[server] Bound after ${attempts} retry(ies).`);
    }
    console.log(`\nMinia server running at http://${HOST}:${port}`);
    console.log(`Dashboard:  http://${HOST}:${port}/`);
    console.log(`Login page: http://${HOST}:${port}/auth-login.html`);
    console.log('Press Ctrl+C to stop.\n');

    const shutdown = (signal) => {
      console.log(`\n[server] Received ${signal}, shutting down...`);
      server.close(() => process.exit(0));
      // Force-exit if close() hangs.
      setTimeout(() => process.exit(0), 2000).unref();
    };
    process.on('SIGINT', () => shutdown('SIGINT'));
    process.on('SIGTERM', () => shutdown('SIGTERM'));
  })
  .catch((err) => {
    console.error(`\n[server] Failed to start: ${err.message}`);
    if (err.code === 'EADDRINUSE') {
      console.error(
        `[server] Tried ports ${PORT}..${PORT + MAX_PORT_ATTEMPTS - 1} — all in use.\n` +
        `        Free one up, or pick a different range, e.g.:\n` +
        `          PORT=4000 npm start`
      );
    }
    process.exit(1);
  });
