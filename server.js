// GET /login — show login form (informational: if currently locked, render
// the 'too many attempts' banner so a refresh shows the user their status)
app.get('/login', (req, res) => {
  if (req.session.userId) return res.redirect('/');
  const guard = loginGuard.check(req.ip);
  res.render('pages/auth-login', {
    title:       'Login',
    error:       null,
    message:     null,
    lockedUntil: guard.allowed ? null : guard.endsAt.toISOString(),
  });
});

// POST /login — authenticate user (with IP-keyed rate-limit + hard lockout)
app.post('/login', async (req, res) => {
  // Pre-flight: don't even touch the DB or bcrypt if this IP is locked.
  // 429 means "real reject" so monitoring tools and clients can distinguish
  // it from generic 401s.
  const guard = loginGuard.check(req.ip);
  if (!guard.allowed) {
    return res.status(429).render('pages/auth-login', {
      title:       'Login',
      error:       null,
      message:     null,
      lockedUntil: guard.endsAt.toISOString(),
    });
  }
  const { email, password } = req.body || {};
  if (!email || !password) {
    // Missing-field attempts don't count as failed credentials — user
    // shouldn't be locked out for an HTML typo.
    return res.status(400).render('pages/auth-login', { title: 'Login', error: 'Email and password are required.', message: null });
  }
  try {
    // Find user by email across all tenants (email is unique per tenant via
    // tenantId_email compound key, so findFirst across tenants works at login).
    const user = await prisma.user.findFirst({
      where: { email },
      // Deep-include rolePermissions so the post-regenerate session can
      // be preloaded with the user's actual permission codes (DB-driven
      // RBAC). Without this `role.rolePermissions` would be undefined
      // and the session would get an empty permissions array, silently
      // locking the user out of every gated route until they re-login.
      include: { tenant: true, role: { include: { rolePermissions: { include: { permission: true } } } } },
    });
    // Combined invalid-because-no-user + invalid-because-bad-password path.
    // Avoids user-enumeration leaks. Either outcome counts as ONE failed
    // attempt against this IP.
    const valid = user ? await bcrypt.compare(password, user.passwordHash) : false;
    if (!user || !valid) {
      loginGuard.recordFail(req.ip);
      return res.status(401).render('pages/auth-login', { title: 'Login', error: 'Invalid email or password.', message: null });
    }
    // Update lastLoginAt — fire-and-forget so it doesn't delay the redirect
    prisma.user.update({ where: { id: user.id }, data: { lastLoginAt: new Date() } }).catch(() => {});
    // Regenerate session to prevent session fixation
    const redirectTo = req.session.returnTo || '/';
    req.session.regenerate(() => {
      req.session.userId   = user.id;
      req.session.tenantId = user.tenantId;
      req.session.tenantSlug = user.tenant ? user.tenant.slug : null;
      req.session.userName = user.fullName;
      req.session.userRole = user.role ? user.role.name.toUpperCase().replace(/ /g, '_') : null;
      // Preload the DB-driven perm codes so can(perm) can gate from
      // session without a per-request DB lookup. The deep include above
      // (role -> rolePermissions -> permission) is what populates this
      // array. Falls back to [] for users with no role or a role with
      // no permissions - can(perm) will then 403 every route, which is
      // the fail-safe behavior (no accidental privilege escalation).
      req.session.userPermissions = user.role && user.role.rolePermissions
        ? user.role.rolePermissions.map(rp => rp.permission.code)
        : [];
      // Successful login — clear the IP's bucket + counter + any lockout.
      loginGuard.clearOnSuccess(req.ip);
      res.redirect(redirectTo);
    });
  } catch (err) {
    console.error('Login error:', err);
    res.status(500).render('pages/auth-login', { title: 'Login', error: 'An error occurred. Please try again.', message: null });
  }
});

// GET /logout — destroy session and redirect to login
app.get('/logout', (req, res) => {
  req.session.destroy(() => {
    res.redirect('/login');
  });
});

// Auth guard middleware — redirects to /login if not authenticated.
// Saves the intended URL so the login handler can redirect back after success.
function requireAuth(req, res, next) {
  if (req.session && req.session.userId) return next();
  req.session.returnTo = req.originalUrl;
  res.redirect('/login');
}

